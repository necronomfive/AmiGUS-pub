    INCLUDE "devices/ahi.i"
    INCLUDE "libraries/ahi_sub.i"

TRUE                                EQU     1
FALSE                               EQU     0
AUDIO_ID_START                      EQU     $0ACF0000

    MACRO LONGVAL
        dc.l \1
    ENDM

    MACRO TAGVAL
        LONGVAL \1
        LONGVAL \2
    ENDM

    MACRO TEXT0
        dc.b \1
        dc.b 0
    ENDM

    MACRO PAD
        CNOP 0,2
    ENDM

    MACRO STARTFORM
        LONGVAL ID_FORM
        LONGVAL FINALIZEFORM-BEGINFORM
BEGINFORM:
        LONGVAL ID_AHIM
    ENDM

    MACRO ENDFORM
FINALIZEFORM:
    ENDM

    MACRO STARTCHUNK
        LONGVAL \1
        LONGVAL FINALIZECHUNK$-BEGINCHUNK$
BEGINCHUNK$:
    ENDM

    MACRO ENDCHUNK
FINALIZECHUNK$:
    PAD
    ENDM

BEG:
    STARTFORM

    STARTCHUNK ID_AUDN
    TEXT0 "AmiGUS"
    ENDCHUNK

